package day11forloopistisna;

public class forloop5 {
    public static void main(String[] args) {
        //7 8 9 carpim sonucunu yaz
        int multiply = 1;

        for (int i=7;i<10;i++){
         multiply=multiply*i;
        }
        System.out.println(multiply);

        //5 6 7 8 9 10   topla sonucunu yaz
        int sum = 0;

        for (int i=5;i<11;i++){
            sum=sum+i;
        }
        System.out.println(sum);


    }//Main
}
