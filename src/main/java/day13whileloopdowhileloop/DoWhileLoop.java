package day13whileloopdowhileloop;

public class DoWhileLoop {
    public static void main(String[] args) {

                                     //WHILELOOP

        int i=1;
        while (i<1){
            System.out.println("whileloop");

            i++;
        }//whileloop sarti olusmadi.Kod calismadi.
        // Zero execution whileloop da mumkundur.

                                     //DOWHILELOOP


        int k=1;
        do {
            System.out.println("DoWhileLoop");
            k++;
        }while (k<1);   //k 1 dedik sart koymadan javaya yap dedik
                       // Sout kodu calisir.Sonrasinda k artar.Tekrar calismaz.
                      //Zero Execution doWhileLoop mumkun degildir.
                      //Body en az bir kez calisir.

    }//min
}
